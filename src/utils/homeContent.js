const STORAGE_KEY = 'carolina_home_content';

// Deep merge so new fields added in code still appear even if the user saved an older version.
function deepMerge(defaults, saved) {
  if (Array.isArray(defaults)) {
    return Array.isArray(saved) ? saved : defaults;
  }
  if (defaults && typeof defaults === 'object') {
    if (!saved || typeof saved !== 'object') return defaults;
    const result = {};
    for (const key of Object.keys(defaults)) {
      result[key] = key in saved ? deepMerge(defaults[key], saved[key]) : defaults[key];
    }
    return result;
  }
  return saved !== undefined ? saved : defaults;
}

export function getHomeContent() {
  try {
    const data = localStorage.getItem(STORAGE_KEY);
    if (!data) return defaultHomeContent;
    return deepMerge(defaultHomeContent, JSON.parse(data));
  } catch {
    return defaultHomeContent;
  }
}

export function saveHomeContent(content) {
  localStorage.setItem(STORAGE_KEY, JSON.stringify(content));
}

export function resetHomeContent() {
  localStorage.removeItem(STORAGE_KEY);
  return defaultHomeContent;
}
